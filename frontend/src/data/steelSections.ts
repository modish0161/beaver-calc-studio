/** Get section by designation string (searches all section types) */
export function findSection(
  designation: string,
): SteelSectionFull | HollowSection | PFCSection | AngleSection | null {
  return (
    UKB_SECTIONS[designation] ??
    UKC_SECTIONS[designation] ??
    PFC_SECTIONS[designation] ??
    CHS_SECTIONS[designation] ??
    SHS_SECTIONS[designation] ??
    RHS_SECTIONS[designation] ??
    ANGLE_SECTIONS[designation] ??
    null
  );
}

/** List all section designations for a given type */
export function listDesignations(
  type: "UKB" | "UKC" | "PFC" | "CHS" | "SHS" | "RHS" | "Angle",
): string[] {
  const db = {
    UKB: UKB_SECTIONS,
    UKC: UKC_SECTIONS,
    PFC: PFC_SECTIONS,
    CHS: CHS_SECTIONS,
    SHS: SHS_SECTIONS,
    RHS: RHS_SECTIONS,
    Angle: ANGLE_SECTIONS,
  };
  return Object.keys(db[type]);
}
