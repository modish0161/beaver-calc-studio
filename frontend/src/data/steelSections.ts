/** Full Universal Beam / Universal Column section properties */
export interface SteelSectionFull {
  /** Serial designation, e.g. '305x165x40' */
  designation: string;
  /** Depth of section (mm) */
  h: number;
  /** Width of section (mm) */
  b: number;
  /** Web thickness (mm) */
  tw: number;
  /** Flange thickness (mm) */
  tf: number;
  /** Root radius (mm) */
  r: number;
  /** Cross-section area (cm²) */
  A: number;
  /** Mass per metre (kg/m) */
  mass: number;
  /** Second moment of area — major axis (cm⁴) */
  Iy: number;
  /** Second moment of area — minor axis (cm⁴) */
  Iz: number;
  /** Plastic modulus — major axis (cm³) */
  Wpl_y: number;
  /** Elastic modulus — major axis (cm³) */
  Wel_y: number;
  /** Plastic modulus — minor axis (cm³) */
  Wpl_z: number;
  /** Elastic modulus — minor axis (cm³) */
  Wel_z: number;
  /** Radius of gyration — major axis (cm) */
  iy: number;
  /** Radius of gyration — minor axis (cm) */
  iz: number;
  /** Warping constant (cm⁶ × 10³) */
  Iw: number;
  /** Torsion constant (cm⁴) */
  It: number;
}

/** Hollow section (CHS / SHS / RHS) properties */
export interface HollowSection {
  designation: string;
  /** Outer dimension or diameter (mm) */
  D: number;
  /** Width — RHS only, 0 for CHS/SHS (mm) */
  B: number;
  /** Wall thickness (mm) */
  t: number;
  /** Cross section area (cm²) */
  A: number;
  /** Mass per metre (kg/m) */
  mass: number;
  /** Second moment of area (cm⁴) — major axis for RHS */
  Iy: number;
  /** Second moment of area (cm⁴) — minor axis for RHS, same as Iy for CHS/SHS */
  Iz: number;
  /** Elastic modulus (cm³) — major axis */
  Wel_y: number;
  /** Plastic modulus (cm³) — major axis */
  Wpl_y: number;
  /** Radius of gyration (cm) — minor */
  i_min: number;
}

/** PFC (Parallel Flange Channel) properties */
export interface PFCSection {
  designation: string;
  h: number;
  b: number;
  tw: number;
  tf: number;
  A: number;
  mass: number;
  Iy: number;
  Iz: number;
}

/** Angle section properties */
export interface AngleSection {
  designation: string;
  /** Leg length (mm) — equal leg */
  a: number;
  /** Thickness (mm) */
  t: number;
  A: number;
  mass: number;
  Iy: number;
  /** Minimum radius of gyration (cm) */
  i_min: number;
}

/** Sheet pile section properties */
export interface SheetPileSection {
  designation: string;
  /** Elastic section modulus per m of wall (cm³/m) */
  Z: number;
  /** Second moment of area per m of wall (cm⁴/m) */
  I: number;
  /** Mass per m² of wall (kg/m²) */
  weight: number;
  description: string;
}

/** Steel grade mechanical properties */
export interface SteelGrade {
  /** Yield strength (MPa) for t ≤ 16mm */
  fy: number;
  /** Ultimate tensile strength (MPa) */
  fu: number;
}

/** Bolt grade mechanical properties */
export interface BoltGrade {
  /** Yield strength (MPa) */
  fyb: number;
  /** Ultimate tensile strength (MPa) */
  fub: number;
}

/** Bolt dimensions */
export interface BoltDimension {
  /** Nominal bolt diameter (mm) */
  d: number;
  /** Tensile stress area (mm²) */
  As: number;
  /** Hole diameter (mm) */
  d0: number;
  /** Head across-flats (mm) */
  dm: number;
}

